package com.java.Dec_10_2023_Day5_ConttrolStatements;
public class WhileLoop {
    // While loop is repeated nested if loop
	// When the no of iterations is not finite it is advised to use While loop
	public static void main(String[] args) {
		int  b= 1;     // initialization
		while(b < 6) {  // test expression or condition
			System.out.println("Hello World" +    b );
			b ++ ; //iteration statemant
		}
	}
		
}
