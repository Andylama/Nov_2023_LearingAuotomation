package com.java.Jan_24_2024_Day18_SpecialClasses_Wrapper_Class;

public class Program2_WrapperClassDemo {

/*      int    >>>    Integer
 *      byte   >>>    Byte                          
 *      short  >>>    Short  
 *      long   >>>    Long      
 *      double >>>    Double 
 *      float  >>>    Float     
 *      char   >>>    Character   
 *            
 *      What are Wrapper Clases ?
 *      pre -defined classes which can contain primitive data type.They wrap primitive data to 
 *      Wrapper Class Object.    
 *                                                                 */
	
	public static void main (String[] args) {
	int A = 10;
	System.out.println("The primitive data type value is :"+ A);
	/*              HereA is Just Variable          */
	
	Integer B = 50;  
	System.out.println("The Wrapper Class Object value is :"+ B);
	/*              Here B is Wrapper Class          */
	
}
}